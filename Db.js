const sqlite = require("sqlite3");
const db = new sqlite.Database("fitness.db");
const createUserTable = `CREATE TABLE IF NOT EXISTS USER (ID INTEGER PRIMARY KEY AUTOINCREMENT,
NAME TEXT NOT NULL,
EMAIL TEXT UNIQUE NOT NULL,
PASSWORD TEXT NOT NULL,
ISADMIN INT)`;

const createGymTable = `CREATE TABLE IF NOT EXISTS GYM (ID INTEGER PRIMARY KEY AUTOINCREMENT,
NAME TEXT NOT NULL,
PRICE INT NOT NULL,
AVAILABLE INT NOT NULL)`;

const createBookingTable = `CREATE TABLE IF NOT EXISTS BOOKING (ID INTEGER PRIMARY KEY AUTOINCREMENT,
USER_ID INT,
GYM_ID INT,
FOREIGN KEY (USER_ID) REFERENCES USER(ID),
FOREIGN KEY (GYM_ID) REFERENCES GYM(ID))`;

module.exports = { db, createUserTable, createGymTable, createBookingTable };
